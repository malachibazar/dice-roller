// value for a face index given a label (d100: 00 -> 100)
export function faceValue(shape, labelIdx) {
  if (shape.labels) {
    const raw = parseInt(shape.labels[labelIdx], 10)
    return shape.label === 'd100' ? (raw === 0 ? 100 : raw) : (raw === 0 ? 10 : raw)
  }
  return labelIdx + 1
}